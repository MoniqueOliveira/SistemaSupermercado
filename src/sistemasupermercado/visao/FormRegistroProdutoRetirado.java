    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        if (produto == null || txtQuantidade.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "É necessário preencher todos os campos para continuar.",
                "Pesquisa sem resultado", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        produtoRetirado = new ProdutoRetirado();
        produtoRetiradoServico = new ProdutoRetiradoServico();

        produtoRetirado.setProduto(produto);
        produtoRetirado.setSessao(sessao);
        produtoRetirado.setMotivo((MotivoProdutoRetirado) cmbMotivo.getSelectedItem());
        produtoRetirado.setQuantidade(new BigDecimal(txtQuantidade.getText().replaceAll(",", ".")));
        produtoRetirado.setObservacao(txtObservacao.getText());

        try {
            produtoRetiradoServico.inserir(produtoRetirado);

            int resposta = JOptionPane.showConfirmDialog(this, "Retirada de produtos registrada com sucesso!\n"
                + "Deseja continuar no registro de saída"
                    + " de produtos?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) redefinir();
            else dispose();
        } catch(RuntimeException ex) {
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha durante a execução.\n" + ex.getMessage(),
                "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGravarActionPerformed

