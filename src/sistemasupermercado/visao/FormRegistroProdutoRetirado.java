    private void exibirListaProdutos() {
        FormListaProdutos form = new FormListaProdutos(null, true);
        form.setVisible(true);
        if (form.getObjetoSelecionado() != null) {
            txtIdProduto.setText(form.getObjetoSelecionado().getIdProduto().toString());
            btnPesquisarProdutoActionPerformed(null);
        }
    }
    
    private void exibirListaMotivos() {
        FormListaMotivoProdutoRetirado form = new FormListaMotivoProdutoRetirado(null, true);
        form.setVisible(true);
        MotivoProdutoRetirado motivo = form.getObjetoSelecionado();
        if (motivo != null) {
            for (int i = 0; i < cmbMotivo.getItemCount(); i++){
                MotivoProdutoRetirado motivoItem = (MotivoProdutoRetirado) cmbMotivo.getItemAt(i);
                if (motivoItem.getIdMotivo().equals(motivo.getIdMotivo())) {
                    cmbMotivo.setSelectedItem(motivoItem);
                    break;
                }
            }
        }
    }

    private void preencherCmbMotivos() {
        MotivoProdutoRetiradoServico m = new MotivoProdutoRetiradoServico();
        try {
            DefaultComboBoxModel<MotivoProdutoRetirado> model = new DefaultComboBoxModel<>(new Vector<>(m.listar()));
            cmbMotivo.setModel(model);
        } catch(RuntimeException ex) {
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha durante a execução.\n" + ex.getMessage(),
                    "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    private void registrarAcoesDosAtalhos() {
        ActionMap actionMapForm = this.rootPane.getActionMap();
        actionMapForm.put("acaof1", acaoF1);
        actionMapForm.put("acaof2", acaoF2);
        actionMapForm.put("acaof3", acaoF3);
        actionMapForm.put("acaof4", acaoF4);
        actionMapForm.put("acaof5", acaoF5);
        rootPane.setActionMap(actionMapForm);
        
        InputMap imapForm = this.rootPane.getInputMap(JPanel.WHEN_IN_FOCUSED_WINDOW);
        imapForm.put(KeyStroke.getKeyStroke("F1"), "acaof1");
        imapForm.put(KeyStroke.getKeyStroke("F2"), "acaof2");
        imapForm.put(KeyStroke.getKeyStroke("F3"), "acaof3");
        imapForm.put(KeyStroke.getKeyStroke("F4"), "acaof4");
        imapForm.put(KeyStroke.getKeyStroke("F5"), "acaof5");
    }

    private void definirOrdemDeTabulação() {
        IndexedFocusTraversalPolicy policy = new IndexedFocusTraversalPolicy();
        policy.addIndexedComponent(txtIdProduto);
        policy.addIndexedComponent(txtQuantidade);
        policy.addIndexedComponent(cmbMotivo);
        setFocusTraversalPolicy(policy);
    }
    
    /**
     * Classe usada para criar as ações dos atalhos do teclado (F1, F2 e F3)
     */
    private class AtalhoAction extends AbstractAction {
	private String atalho;

	public AtalhoAction(String atalho)
	{
		super(atalho);
		this.atalho = atalho;
	}
        
        @Override
        public void actionPerformed(ActionEvent ae) {
            switch(atalho) {
                case ("F1"):
                    btnPesquisarProdutoActionPerformed(ae);
                    break;
                case ("F2"):
                    exibirListaProdutos();
                    break;
                case ("F3"):
                    btnNovoActionPerformed(ae);
                    break;
                case ("F4"):
                    btnGravarActionPerformed(ae);
                    break;
                case ("F5"):
                    exibirListaMotivos();
            }
        }

    }
