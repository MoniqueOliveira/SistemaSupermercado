    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        if (produto == null || fornecedor == null || txtQuantidade.getText().equals("") || txtValor.equals("")) {
            JOptionPane.showMessageDialog(this, "É necessário preencher todos os campos para continuar.", 
                        "Pesquisa sem resultado", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        entradaProduto = new EntradaProduto();
        entradaProdutoServico = new EntradaProdutoServico();
        
        entradaProduto.setFornecedor(fornecedor);
        entradaProduto.setProduto(produto);
        entradaProduto.setSessao(sessao);
        entradaProduto.setValorUnitario(new BigDecimal(txtValor.getText().replaceAll(",", ".")));
        entradaProduto.setQuantidade(new BigDecimal(txtQuantidade.getText().replaceAll(",", ".")));
        
        try {
            entradaProdutoServico.inserir(entradaProduto);
            
            int resposta = JOptionPane.showConfirmDialog(this, "Entrada de produtos registrada com sucesso!\n"
                    + "Deseja continuar no registro de entrada de produtos?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) redefinir();
            else dispose();
        } catch(RuntimeException ex) {
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha durante a execução.\n" + ex.getMessage(), 
                        "Atenção", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnGravarActionPerformed

